public class VariablePractice
{
	public static void main(String[] args)
	{
		String name = "jeremy";
		
		int length = 5;
		int width = 10;
		
		int radius = 6;
		double pi = 3.14;
		
		System.out.println(name);
		System.out.println(length * width); //rectangle area
		System.out.println(2 * pi * radius); //circle circumference
		System.out.println(pi * radius * radius); //circle area
		
	}
}
